class Solution(object):
    def climbStairs(self, n):
        """
        if n = 1: return 1
        if n = 2: return 2
        if n = 3: return f(2) + f(1) = 3
        ...
        if n = 7: return f(6) + f(5)
        if n = n: return f(n-1) + f(n-2)
        so, f(n) = f(n-1) + f(n-2)
        :type n: int
        :rtype: int
        """
        dp = []
        dp.append(1)
        dp.append(2)
        for i in range(2, n):  # i is index of dp
            dp.append(dp[i - 1] + dp[i - 2])
        return dp[n - 1]
